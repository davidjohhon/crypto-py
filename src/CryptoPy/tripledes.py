class DES(BlockCipher):
    """
    DES block cipher.

    Key size: 64 bits (8 bytes / 2 words).
    Block size: 64 bits (2 words).
    IV size: 64 bits.
    16 Feistel rounds with 48-bit subkeys.
    """

    keySize = 64 // 32
    ivSize = 64 // 32
    blockSize = 64 // 32

    def _doReset(self):
        """
        Generate 16 round subkeys from the 64-bit key.

        The key bits are permuted via PC1 to 56 bits, split into
        two 28-bit halves, then each round shifts the halves and
        applies PC2 to select 48 subkey bits.
        """
        key = self._key
        keyWords = key.words

        keyBits = []
        for i in range(56):
            keyBitPos = PC1[i] - 1
            keyBits.append((keyWords[keyBitPos >> 5] >> (31 - keyBitPos % 32)) & 1)

        subKeys = self._subKeys = []
        for nSubKey in range(16):
            subKey = [0] * 8
            bitShift = BIT_SHIFTS[nSubKey]
            for i in range(24):
                subKey[i // 6] |= keyBits[((PC2[i] - 1) + bitShift) % 28] << (31 - i % 6)
                subKey[4 + (i // 6)] |= keyBits[28 + (((PC2[i + 24] - 1) + bitShift) % 28)] << (31 - i % 6)
            subKey[0] = _32((subKey[0] << 1) | (subKey[0] >> 31))
            for i in range(1, 7):
                subKey[i] = subKey[i] >> ((i - 1) * 4 + 3)
            subKey[7] = _32((subKey[7] << 5) | (subKey[7] >> 27))
            subKeys.append(subKey)

        self._invSubKeys = []
        for i in range(16):
            self._invSubKeys.append(subKeys[15 - i])

    def encryptBlock(self, M, offset):
        """Encrypt one 64-bit (2-word) block."""
        self._doCryptBlock(M, offset, self._subKeys)

    def decryptBlock(self, M, offset):
        """Decrypt one 64-bit (2-word) block using reversed subkeys."""
        self._doCryptBlock(M, offset, self._invSubKeys)

    def _doCryptBlock(self, M, offset, subKeys):
        """
        Core DES Feistel network (16 rounds).

        Initial and final permutations are implemented via bit
        exchange operations (exchangeLR/exchangeRL) that reorder
        bits using a series of XOR-swaps with masks.

        Each Feistel round:
          1. XOR the right half with the round subkey.
          2. Run through 8 S-boxes (SBOX_P precomputed lookup).
          3. XOR the result with the left half.
          4. Swap left and right.
        """
        self._lBlock = M[offset]
        self._rBlock = M[offset + 1]

        self._exchangeLR(4, 0x0F0F0F0F)
        self._exchangeLR(16, 0x0000FFFF)
        self._exchangeRL(2, 0x33333333)
        self._exchangeRL(8, 0x00FF00FF)
        self._exchangeLR(1, 0x55555555)

        for round in range(16):
            subKey = subKeys[round]
            lBlock = self._lBlock
            rBlock = self._rBlock
            f = 0
            for i in range(8):
                f |= SBOX_P[i][((rBlock ^ subKey[i]) & SBOX_MASK[i])]
            self._lBlock = rBlock
            self._rBlock = _32(lBlock ^ f)

        t = self._lBlock
        self._lBlock = self._rBlock
        self._rBlock = t

        self._exchangeLR(1, 0x55555555)
        self._exchangeRL(8, 0x00FF00FF)
        self._exchangeRL(2, 0x33333333)
        self._exchangeLR(16, 0x0000FFFF)
        self._exchangeLR(4, 0x0F0F0F0F)

        M[offset] = self._lBlock
        M[offset + 1] = self._rBlock

    def _exchangeLR(self, offset, mask):
        """
        Exchange bits between left and right halves.

        (left >> offset) XOR right, masked, is applied to both halves.
        """
        t = ((self._lBlock >> offset) ^ self._rBlock) & mask
        self._rBlock ^= t
        self._lBlock ^= t << offset

    def _exchangeRL(self, offset, mask):
        """
        Exchange bits between right and left halves.

        (right >> offset) XOR left, masked, is applied to both halves.
        """
        t = ((self._rBlock >> offset) ^ self._lBlock) & mask
        self._lBlock ^= t
        self._rBlock ^= t << offset


class TripleDES(BlockCipher):
    """
    TripleDES (3DES) block cipher, EDE mode.

    Key size: 192 bits (24 bytes / 6 words) — three 64-bit keys.
    With a 128-bit key (4 words), the third key equals the first.
    With a 64-bit key (2 words), all three keys are identical.
    Block size: 64 bits (2 words).
    IV size: 64 bits.

    Operation: Encrypt with key1, Decrypt with key2, Encrypt with key3.
    """

    keySize = 192 // 32
    ivSize = 64 // 32
    blockSize = 64 // 32

    def _doReset(self):
        """
        Set up three internal DES instances for EDE.

        Key material is split into 1, 2, or 3 independent DES keys
        depending on the supplied key length.
        """
        key = self._key
        keyWords = key.words
        if len(keyWords) not in (2, 4) and len(keyWords) < 6:
            raise ValueError('Invalid key length - 3DES requires the key length to be 64, 128, 192 or >192.')

        key1 = keyWords[:2]
        key2 = keyWords[:2] if len(keyWords) < 4 else keyWords[2:4]
        key3 = keyWords[:2] if len(keyWords) < 6 else keyWords[4:6]

        self._des1 = DES.createEncryptor(WordArray.create(key1))
        self._des2 = DES.createEncryptor(WordArray.create(key2))
        self._des3 = DES.createEncryptor(WordArray.create(key3))

    def encryptBlock(self, M, offset):
        """Encrypt: DES(key1) → DES^{-1}(key2) → DES(key3)."""
        self._des1.encryptBlock(M, offset)
        self._des2.decryptBlock(M, offset)
        self._des3.encryptBlock(M, offset)

    def decryptBlock(self, M, offset):
        """Decrypt: DES^{-1}(key3) → DES(key2) → DES^{-1}(key1)."""
        self._des3.decryptBlock(M, offset)
        self._des2.encryptBlock(M, offset)
        self._des1.decryptBlock(M, offset)
