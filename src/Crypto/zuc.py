class ZUC(StreamCipher):
    keySize = 128 // 32
    ivSize = 128 // 32
    blockSize = 1

    def _doReset(self):
        key = self._key
        iv = getattr(self.cfg, 'iv', None) or WordArray.create([0]*4)

        k = [(key.words[i] >> (24-8*j)) & 0xFF for i in range(4) for j in range(4)]
        ivb = [(iv.words[i] >> (24-8*j)) & 0xFF for i in range(4) for j in range(4)]

        self._LFSR = [(k[i] << 23) | (D[i] << 8) | ivb[i] for i in range(16)]
        self._R1 = 0
        self._R2 = 0

        for _ in range(32):
            f = self._F(self._LFSR[0], self._LFSR[8])
            w = (f ^ self._R1) & 0x7FFFFFFF
            self._R1_update(f)
            self._R2_update(f)
            v = self._LFSR_backward()
            self._LFSR_shift(v, w)

    def _F(self, X0, X1):
        w = _32((X0 << 16) | (X1 >> 16))
        w1 = self._R1 ^ w
        w2 = self._R2 ^ w
        return _32(self._L1(w1) + self._L2(w2))

    @staticmethod
    def _L1(x):
        return x ^ (x >> 2) ^ (x >> 10) ^ (x >> 18) ^ (x >> 24)

    @staticmethod
    def _L2(x):
        return x ^ (x >> 8) ^ (x >> 14) ^ (x >> 22) ^ (x >> 30)

    @staticmethod
    def _make_u32(a, b, c, d):
        return (a << 24) | (b << 16) | (c << 8) | d

    def _R1_update(self, f):
        X = self._R1 + f
        self._R1 = self._make_u32(S0[(X >> 24) & 0xFF], S1[(X >> 16) & 0xFF],
                                  S0[(X >> 8) & 0xFF], S1[X & 0xFF])

    def _R2_update(self, f):
        X = self._R2 + f
        self._R2 = self._make_u32(S0[(X >> 24) & 0xFF], S1[(X >> 16) & 0xFF],
                                  S0[(X >> 8) & 0xFF], S1[X & 0xFF])

    def _LFSR_backward(self):
        v = (_32(self._LFSR[0] << 8) | urs(self._LFSR[0], 15)) ^ \
            (_32(self._LFSR[2] << 17) | urs(self._LFSR[2], 14)) ^ \
            (_32(self._LFSR[3] << 5)  | urs(self._LFSR[3], 26)) ^ \
            (_32(self._LFSR[5] << 21) | urs(self._LFSR[5], 10)) ^ \
            (_32(self._LFSR[7] << 7)  | urs(self._LFSR[7], 24)) ^ \
            (_32(self._LFSR[11] << 8) | urs(self._LFSR[11], 23)) ^ \
            (_32(self._LFSR[13] << 12)| urs(self._LFSR[13], 19)) ^ \
            (_32(self._LFSR[14] << 13)| urs(self._LFSR[14], 18))
        return v & 0x7FFFFFFF

    def _LFSR_shift(self, v, w):
        v = (v + (w >> 1)) & 0x7FFFFFFF
        self._LFSR[:-1] = self._LFSR[1:]
        self._LFSR[15] = v

    def _doProcessBlock(self, M, offset):
        f = self._F(self._LFSR[0], self._LFSR[8])
        Z = f ^ self._R1
        M[offset] = _32(M[offset] ^ Z)
        w = (f ^ self._R1) & 0x7FFFFFFF
        self._R1_update(f)
        self._R2_update(f)
        v = self._LFSR_backward()
        self._LFSR_shift(v, w)
